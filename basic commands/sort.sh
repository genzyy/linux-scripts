# Used to sort the contents of the file grammatically.

sort sort.txt

# To save the output in a new file output.txt
sort -o output_file.txt sort.txt

# To sort in the reverse order
sort -r sort.txt 

# To sort the numeric data stored in a file, we use
sort -n sort.txt

#  Note : If there is no output then the file is considered to be already sorted 